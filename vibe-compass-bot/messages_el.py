def get_insight_message(pain_point, time_spent):
    """Δημιουργεί εξατομικευμένο μήνυμα με insight"""
    insight = INSIGHTS.get(pain_point)

    if not insight:
        return "Δεν ήταν δυνατός ο προσδιορισμός του προβλήματος. Δοκίμασε ξανά."

    # Καθορίζουμε το χρονικό διάστημα
    time_map = {
        'time_low': 'time_low',
        'time_medium': 'time_medium',
        'time_high': 'time_high'
    }
    time_key = time_map.get(time_spent, 'time_medium')
    time_text = insight['time_calculation'][time_key]

    # Σχηματίζουμε το κείμενο των λύσεων
    solutions_text = '\n'.join(insight['solutions'])

    message = f"""Λοιπόν, πιάνουμε το vibe... ✨

Ο κύριος "καταναλωτής ενέργειας" σου είναι **{insight['title']}**.

Ξοδεύεις περίπου **{time_text}**! Φαντάσου ότι θα είχες ακόμα μία εργάσιμη εβδομάδα.

**Εδώ είναι τι μπορούμε να κάνουμε vibe-code πρώτα:**

{solutions_text}

**Αποτέλεσμα:** {insight['result']}"""

    return message


